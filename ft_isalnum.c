int	ft_isalnum(int k)
{
	unsigned char	i;

	i = (unsigned char)k;
	if (('a' <= i && i <= 'z') || ('A' <= i && i <= 'Z'))
		return (1024);
	else if ('0' <= i && i <= '9')
		return (1024);
	else
		return (0);
}
